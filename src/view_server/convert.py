def hex_to_move(amz, hex):
    tmp = [None for _ in range(3)]
    ctr = 2
    while ctr != -1:
        i = (hex & 0xF0) >> 4
        j = hex & 0x0F
        tmp[ctr] = (i, j)
        hex >>= 8
        ctr -= 1
    ind = amz.get_amazon_ind(*tmp[0])
    act = [
        (tmp[1][0] - tmp[0][0], tmp[1][1] - tmp[0][1]),
        (tmp[2][0] - tmp[1][0], tmp[2][1] - tmp[1][1]),
    ]
    return ind * 5184 + ACT2ID[act_to_str(act)]